let num = [5, 8, 4] //para criar um vetor basta usar []

num[3] = 6 //para criar uma nova "vaga"
num.push(7) //para adicionar um elemento sem se importar com a posição, indo automaticamente pro final
num.length //para saber quantos elementes possui no vetor
num.sort() //para deixar todos os elementos em ordem crescente

//para mostrar um valor específico do vetor basta usar num[valor do elemento]

console.log(`Os meus vetores são ${num}`)

